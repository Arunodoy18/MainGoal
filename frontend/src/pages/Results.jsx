import { motion } from "framer-motion";
import { useLocation, useNavigate, Navigate } from "react-router-dom";
import { ChevronLeft, Activity, AlertTriangle, ShieldCheck, Download } from "lucide-react";
import { RadialBarChart, RadialBar, PolarAngleAxis, ResponsiveContainer } from "recharts";

const containerVariants = {
  hidden: { opacity: 0 },
  show: {
    opacity: 1,
    transition: { staggerChildren: 0.15 },
  },
};

const itemVariants = {
  hidden: { opacity: 0, y: 20 },
  show: { opacity: 1, y: 0, transition: { type: "spring", stiffness: 120 } },
};

export default function Results() {
  const { state } = useLocation();
  const navigate = useNavigate();

  if (!state?.result) {
    return <Navigate to="/form" replace />;
  }

  const { surgery_required, confidence, risk_level, top_features, llm_explanation } = state.result;

  const downloadReport = () => {
    const reportText = `PRAANA-LBP - CLINICAL AI SURGERY PREDICTION REPORT
--------------------------------------------------
Risk Level: ${risk_level}
Confidence: ${confidence.toFixed(2)}%
Verdict: ${surgery_required ? 'SURGERY REQUIRED' : 'CONSERVATIVE MANAGEMENT'}

Top Biomarkers Driving Prediction:
${top_features.join(', ')}

CLINICAL PROFILE
${llm_explanation?.patient_summary}

PREDICTION RATIONALE
${llm_explanation?.prediction_reason}

RECOMMENDED NEXT STEPS
${llm_explanation?.next_steps}

--------------------------------------------------
Generated by PRAANA-LBP Platform`;

    const element = document.createElement("a");
    const file = new Blob([reportText], { type: "text/plain" });
    element.href = URL.createObjectURL(file);
    element.download = "PRAANA-LBP-Clinical-Report.txt";
    document.body.appendChild(element);
    element.click();
    document.body.removeChild(element);
  };

  const riskColors = {
    HIGH: "text-red-400 drop-shadow-[0_0_12px_rgba(248,113,113,0.8)]",
    MEDIUM: "text-yellow-400 drop-shadow-[0_0_12px_rgba(250,204,21,0.8)]",
    LOW: "text-emerald-400 drop-shadow-[0_0_12px_rgba(52,211,153,0.8)]",
  };

  const riskBg = {
    HIGH: "fill-red-500",
    MEDIUM: "fill-yellow-500",
    LOW: "fill-emerald-500",
  };

  const chartData = [{ name: "Confidence", value: confidence, fill: "currentColor" }];

  return (
    <div className="min-h-screen bg-[#030812] px-4 py-12 text-text">
      <div className="mx-auto max-w-5xl">
        <button
          onClick={() => navigate("/form")}
          className="mb-8 flex items-center gap-2 text-muted transition hover:text-accent"
        >
          <ChevronLeft size={20} />
          <span>Back to Diagnosis</span>
        </button>

        <motion.div
          variants={containerVariants}
          initial="hidden"
          animate="show"
          className="grid gap-8 lg:grid-cols-12"
        >
          {/* Main Verdict Panel - Left Column */}
          <motion.div variants={itemVariants} className="col-span-12 lg:col-span-5">
            <div className="card-border flex h-full flex-col items-center justify-center rounded-3xl bg-panel/60 p-8 shadow-glow backdrop-blur-xl text-center">
              
              <div className={`mb-4 text-3xl font-black tracking-wider ${riskColors[risk_level]}`}>
                {risk_level} RISK
              </div>
              
              {/* Circular Progress Gauge */}
              <div className="relative mb-6 flex h-48 w-48 items-center justify-center">
                <ResponsiveContainer width="100%" height="100%" className="-rotate-90">
                  <RadialBarChart
                    cx="50%"
                    cy="50%"
                    innerRadius="80%"
                    outerRadius="100%"
                    barSize={12}
                    data={chartData}
                    startAngle={0}
                    endAngle={360}
                  >
                    <PolarAngleAxis type="number" domain={[0, 100]} angleAxisId={0} tick={false} />
                    <RadialBar
                      minAngle={15}
                      background={{ fill: "rgba(255,255,255,0.05)" }}
                      clockWise
                      dataKey="value"
                      cornerRadius={10}
                      className={riskBg[risk_level]}
                    />
                  </RadialBarChart>
                </ResponsiveContainer>
                <div className="absolute inset-0 flex flex-col items-center justify-center">
                  <span className="text-4xl font-bold text-white">{Math.round(confidence)}%</span>
                  <span className="text-xs tracking-widest text-muted">CONFIDENCE</span>
                </div>
              </div>

              <div className="flex w-full items-center justify-center gap-3 rounded-2xl bg-panelSoft py-4 px-6 ring-1 ring-white/5">
                {surgery_required ? (
                  <AlertTriangle className="text-red-400" size={28} />
                ) : (
                  <ShieldCheck className="text-emerald-400" size={28} />
                )}
                <span className="text-xl font-bold text-white tracking-wide">
                  {surgery_required ? "SURGERY REQUIRED" : "CONSERVATIVE MGT"}
                </span>
              </div>
              
              <button
                onClick={downloadReport}
                className="mt-8 flex w-full items-center justify-center gap-2 rounded-xl bg-white/5 px-6 py-4 font-semibold text-white ring-1 ring-white/20 transition-all hover:bg-white/10"
              >
                <Download size={20} />
                Download Report (.txt)
              </button>
            </div>
          </motion.div>

          {/* AI Explanation & Top Features - Right Column */}
          <motion.div variants={itemVariants} className="col-span-12 space-y-8 lg:col-span-7">
            
            {/* Top Biomarkers */}
            <div className="card-border rounded-3xl bg-panel/60 p-8 backdrop-blur-xl">
              <div className="mb-5 flex items-center gap-3">
                <Activity className="text-accent" />
                <h3 className="text-xl font-bold text-white">Driving Biomarkers</h3>
              </div>
              <div className="flex flex-wrap gap-3">
                {top_features.map((feature, idx) => (
                  <span
                    key={idx}
                    className="rounded-full bg-accent/10 px-4 py-2 text-sm font-semibold tracking-wide text-accent ring-1 ring-accent/30 shadow-[0_0_15px_rgba(0,229,255,0.15)]"
                  >
                    {feature}
                  </span>
                ))}
              </div>
            </div>

            {/* AI Summary Cards */}
            <div className="grid gap-4 sm:grid-cols-1">
              <motion.div variants={itemVariants} className="card-border rounded-2xl bg-panelSoft p-6">
                <h4 className="mb-2 text-sm font-bold tracking-widest text-secondary">CLINICAL PROFILE</h4>
                <p className="text-muted leading-relaxed">{llm_explanation?.patient_summary}</p>
              </motion.div>

              <motion.div variants={itemVariants} className="card-border rounded-2xl bg-panelSoft p-6">
                <h4 className="mb-2 text-sm font-bold tracking-widest text-secondary">PREDICTION RATIONALE</h4>
                <p className="text-muted leading-relaxed">{llm_explanation?.prediction_reason}</p>
              </motion.div>

              <motion.div variants={itemVariants} className="card-border rounded-2xl bg-panelSoft p-6">
                <h4 className="mb-2 text-sm font-bold tracking-widest text-accent">RECOMMENDED NEXT STEPS</h4>
                <p className="text-white leading-relaxed">{llm_explanation?.next_steps}</p>
              </motion.div>
            </div>

          </motion.div>
        </motion.div>
      </div>
    </div>
  );
}
